    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        try{
            DefaultTableModel model = (DefaultTableModel) delTable.getModel();
            int SelectedRowIndex = delTable.getSelectedRow();
            String Eid =  model.getValueAt(SelectedRowIndex, 0).toString();
            try{
                LoginDataObject myObject = new LoginDataObject();
                myObject.setMessage("DeleteEmployee");
                myObject.setEid(Eid);
                Socket socketToServer = new Socket("127.0.0.1", 3000);
                ObjectOutputStream myOutputStream =
                                        new ObjectOutputStream(socketToServer.getOutputStream());
                ObjectInputStream myInputStream =
                                        new ObjectInputStream(socketToServer.getInputStream());
                myOutputStream.writeObject(myObject);
                myObject = (LoginDataObject)myInputStream.readObject();

                Boolean err = myObject.getError();
                if(!err){
                    JOptionPane.showMessageDialog(this,"Deleted successfully","Error",JOptionPane.ERROR_MESSAGE);
                }
                    myOutputStream.close();
                    myInputStream.close();
                    socketToServer.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"d","Error",JOptionPane.ERROR_MESSAGE);

        }
       
        model.removeRow(SelectedRowIndex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error");
        }
        
    }//GEN-LAST:event_deleteButtonActionPerformed

