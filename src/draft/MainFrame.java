    private void searchButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButton1ActionPerformed

        // TODO add your handling code here:
        String empId_value = empIdText1.getText();
        String empName_value = empNameText1.getText();
             
        try{   
           LoginDataObject myObject = new LoginDataObject();
           myObject.setMessage("EmpSearch");
           myObject.setEid(empId_value);
           myObject.setEname(empName_value);
           Socket socketToServer = new Socket("127.0.0.1", 3000);
            ObjectOutputStream myOutputStream =
                                    new ObjectOutputStream(socketToServer.getOutputStream());
            ObjectInputStream myInputStream =
                                    new ObjectInputStream(socketToServer.getInputStream());
            myOutputStream.writeObject(myObject);
    
            myObject = (LoginDataObject)myInputStream.readObject();
                
                Boolean err = myObject.getError();
                List<String> id = (ArrayList<String>)myObject.getEmpID();
                List<String> fn=(ArrayList<String>)myObject.getFname();
                List<String> ln=(ArrayList<String>)myObject.getLname();
                List<String> gen=(ArrayList<String>)myObject.getGender();
                List<String> ph=(ArrayList<String>)myObject.getPhone();
                List<String> em=(ArrayList<String>)myObject.getEmail();
                List<String> st=(ArrayList<String>)myObject.getStreet();
                List<String> ap=(ArrayList<String>)myObject.getApt();
                List<String> ci=(ArrayList<String>)myObject.getCity();
                List<String> stt=(ArrayList<String>)myObject.getState();
                List<String> zp=(ArrayList<String>)myObject.getZip();
                Integer cnt = myObject.getCount();
            
            if(err){
                JOptionPane.showMessageDialog(this,"No Employee record with this combination","Error",JOptionPane.ERROR_MESSAGE);
            }
            else{   
                
                for(int row = 0; row <cnt; row++){
                DefaultTableModel model = (DefaultTableModel) searchTable1.getModel();
                model.addRow(new Object[]{
                        id.get(row),
                        fn.get(row),
                        ln.get(row),
                        gen.get(row),
                        ph.get(row),
                        em.get(row),
                        st.get(row),
                        ap.get(row),
                        ci.get(row),
                        stt.get(row),
                        zp.get(row),
                    });

                }
            }
            
                myOutputStream.close();
                myInputStream.close();
                socketToServer.close();
            
        }catch(HeadlessException | IOException | ClassNotFoundException e){
            JOptionPane.showMessageDialog(this,"d","Error",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_searchButton1ActionPerformed

